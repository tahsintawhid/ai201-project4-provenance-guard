@app.route("/analytics", methods=["GET"])
def analytics():
    conn = get_connection()
    cursor = conn.cursor()

    cursor.execute("SELECT COUNT(*) FROM audit_log")
    total_submissions = cursor.fetchone()[0]

    cursor.execute("SELECT attribution_result, COUNT(*) FROM audit_log GROUP BY attribution_result")
    verdict_counts = {row[0]: row[1] for row in cursor.fetchall()}

    cursor.execute("SELECT COUNT(*) FROM appeals")
    total_appeals = cursor.fetchone()[0]

    appeal_rate = round(total_appeals / total_submissions, 4) if total_submissions > 0 else 0.0

    cursor.execute("SELECT AVG(combined_score) FROM audit_log")
    avg_confidence = cursor.fetchone()[0]
    avg_confidence = round(avg_confidence, 4) if avg_confidence else 0.0

    cursor.execute("""
        SELECT AVG(combined_score) FROM audit_log
        WHERE submitted_at >= datetime('now', '-1 hour')
    """)
    avg_confidence_last_hour = cursor.fetchone()[0]
    avg_confidence_last_hour = round(avg_confidence_last_hour, 4) if avg_confidence_last_hour else 0.0

    cursor.execute("""
        SELECT attribution_result, COUNT(*) as count
        FROM audit_log
        WHERE submitted_at >= datetime('now', '-1 hour')
        GROUP BY attribution_result
    """)
    recent_verdicts = {row[0]: row[1] for row in cursor.fetchall()}

    conn.close()

    ai_count = verdict_counts.get("likely_ai", 0)
    human_count = verdict_counts.get("likely_human", 0)
    uncertain_count = verdict_counts.get("uncertain", 0)

    return jsonify({
        "total_submissions": total_submissions,
        "verdict_breakdown": {
            "likely_ai": ai_count,
            "likely_human": human_count,
            "uncertain": uncertain_count
        },
        "ai_ratio": round(ai_count / total_submissions, 4) if total_submissions > 0 else 0.0,
        "human_ratio": round(human_count / total_submissions, 4) if total_submissions > 0 else 0.0,
        "uncertain_ratio": round(uncertain_count / total_submissions, 4) if total_submissions > 0 else 0.0,
        "total_appeals": total_appeals,
        "appeal_rate": appeal_rate,
        "avg_confidence_score_all_time": avg_confidence,
        "avg_confidence_score_last_hour": avg_confidence_last_hour,
        "recent_verdict_breakdown_last_hour": recent_verdicts
    }), 200


